def expand_user(path: str) -> Path:
    if path == "~":
        return HOME
    if path.startswith("~/"):
        return HOME / path[2:]
    return Path(path)


def path_exists(path: str) -> bool:
    return expand_user(path.rstrip("/")).exists()


def should_render_watch(watch: Watch, ctx: RenderContext) -> bool:
    return ctx.default_template or watch.always_render or path_exists(watch.path)


def readlink_f(path: str) -> str:
    return os.path.realpath(os.path.expanduser(path))


def which(command: str) -> str | None:
    found = shutil.which(command)
    if found is None:
        return None
    return readlink_f(found)


def prefixed(value: str, entry: Entry) -> str:
    rendered = value + entry.suffix
    if entry.filter:
        rendered = f"{entry.filter}:{rendered}"
    return rendered


def resolve_default_entries(entry: Entry) -> list[RenderedEntry]:
    if entry.value is not None:
        return [RenderedEntry(entry.value, True, entry.comment)]

    if entry.command is None:
        raise ValueError("entry must define value or command")

    paths = entry.standard_paths or (f"/usr/bin/{entry.command}",)
    return [RenderedEntry(prefixed(path, entry), True, entry.comment) for path in paths]


def resolve_entry(entry: Entry, watch: Watch, ctx: RenderContext) -> RenderedEntry:
    if entry.value is not None:
        active = path_exists(entry.value) if entry.value.startswith(("~", "/")) else path_exists(entry.value.split(":", 1)[-1])
        return RenderedEntry(entry.value, active, entry.comment if active else entry.missing_comment)

    if entry.command is None:
        raise ValueError("entry must define value or command")

    for standard_path in entry.standard_paths:
        if Path(standard_path.rstrip("/")).exists():
            return RenderedEntry(prefixed(readlink_f(standard_path), entry), True, entry.comment)

    discovered = which(entry.command)
    if discovered is not None:
        warning = (
            f"{watch.path}: {entry.command} resolved to non-standard path {discovered}; "
            f"standard paths checked: {', '.join(entry.standard_paths)}"
        )
        ctx.warn(warning)
        comment = "WARNING: non-standard path found via PATH"
        if entry.comment:
            comment = f"{entry.comment}; {comment}"
        return RenderedEntry(prefixed(discovered, entry), True, comment)

    missing = entry.standard_paths[0] if entry.standard_paths else f"/usr/bin/{entry.command}"
    return RenderedEntry(prefixed(missing, entry), False, entry.missing_comment)


def quote(value: str) -> str:
    return '"' + value.replace("\\", "\\\\").replace('"', '\\"') + '"'


def render_entry(entry: RenderedEntry) -> str:
    prefix = "    " if entry.active else "    # "
    line = f"{prefix}{quote(entry.value)},"
    if entry.comment:
        line += f"  # {entry.comment}"
    return line


def dedupe_key(value: str) -> str:
    filter_prefix = ""
    path_part = value
    if ":" in value and not value.startswith("/"):
        filter_value, path_part = value.split(":", 1)
        filter_prefix = f"{filter_value}:"

    profile_suffix = ""
    if ";" in path_part:
        path_part, profile_name = path_part.split(";", 1)
        profile_suffix = f";{profile_name}"

    trailing_slash = path_part.endswith("/")
    if path_part.startswith(("~", "/")):
        path_part = str(expand_user(path_part.rstrip("/")))
        if trailing_slash:
            path_part += "/"

    return f"{filter_prefix}{path_part}{profile_suffix}"


def render_watch(watch: Watch, ctx: RenderContext) -> str:
    lines = [f"# --- {watch.title} ---", f"{quote(watch.path)} = ["]
    rendered_entries: list[RenderedEntry] = []
    seen: set[str] = set()
    for entry in watch.entries:
        entries = resolve_default_entries(entry) if ctx.default_template else [resolve_entry(entry, watch, ctx)]
        for rendered in entries:
            key = dedupe_key(rendered.value)
            if key in seen:
                continue
            seen.add(key)
            rendered_entries.append(rendered)
    lines.extend(render_entry(entry) for entry in rendered_entries)
    lines.append("]")
    return "\n".join(lines)


def render_section(title: str, watches: Iterable[Watch], ctx: RenderContext) -> str:
    blocks = [render_watch(watch, ctx) for watch in watches if should_render_watch(watch, ctx)]
    if not blocks:
        return title
    return title + "\n\n" + "\n\n".join(blocks)


