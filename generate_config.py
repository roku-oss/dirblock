@dataclass(frozen=True)
class Entry:
    value: str | None = None
    command: str | None = None
    standard_paths: tuple[str, ...] = ()
    suffix: str = ""
    filter: str | None = None
    comment: str | None = None
    missing_comment: str = "not found"


@dataclass(frozen=True)
class Watch:
    path: str
    title: str
    entries: tuple[Entry, ...]
    check_paths: tuple[str, ...] = ()
    always_render: bool = False


@dataclass
class RenderedEntry:
    value: str
    active: bool
    comment: str | None = None


@dataclass
class RenderContext:
    default_template: bool = False
    warnings: list[str] = field(default_factory=list)

    def warn(self, message: str) -> None:
        self.warnings.append(message)


